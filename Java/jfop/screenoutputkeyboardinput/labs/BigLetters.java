//Write a program that prints out your initials as a large banner, with each large letter made up of the same letter. Don't prompt the user to enter initials, just hard code your initials. Here's what we mean for the output:
public class BigLetters
{
  public static void main(String[] args)
  {
    printFirstInitial();
    printMiddleInitial();
    printLastInitial();
  }
  public static void printFirstInitial()
  {
    System.out.println("RRRRRRR");
    System.out.println("R      R");
    System.out.println("R      R");
    System.out.println("RRRRRRR");
    System.out.println("R      R");
    System.out.println("R       R");
    System.out.println("R        R");
  }
  public static void printMiddleInitial()
  {
    System.out.println("   A   ");
    System.out.println("  A A  ");
    System.out.println(" A   A ");
    System.out.println("AAAAAAA");
    System.out.println("A     A");
    System.out.println("A     A");
    System.out.println("A     A");
  }
  public static void printLastInitial()
  {
    System.out.println("   A   ");
    System.out.println("  A A  ");
    System.out.println(" A   A ");
    System.out.println("AAAAAAA");
    System.out.println("A     A");
    System.out.println("A     A");
    System.out.println("A     A");
  }
}
